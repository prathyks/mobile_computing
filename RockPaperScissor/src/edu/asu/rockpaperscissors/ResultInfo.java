package edu.asu.rockpaperscissors;

public class ResultInfo{
	int wins;
	int loses;
	int draws;
	public ResultInfo() {
	}
	public int getWins() {
		return wins;
	}
	public void setWins(int wins) {
		this.wins = wins;
	}
	public int getLoses() {
		return loses;
	}
	public void setLoses(int loses) {
		this.loses = loses;
	}
	public int getDraws() {
		return draws;
	}
	public void setDraws(int draws) {
		this.draws = draws;
	}		
	
}
